    <h4><a name="short-story"></a>Short story </h4>
<ul>
  <li>News related to your office or work (report, staff initiative etc.)</li>
  <li>150 words maximum</li>
  <li>Pre-book a time slot with our team</li>
  <li>Preferably in both English and French</li>
  <li>Allow 24 to 48 hours for publication (depending on availability, pre-booking is advisable)</li>
  <li>Send up to five pictures (800 pixels minimum and landscape)</li>
  <li>Content will be reviewed and/or edited by the UN Intranet team</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a>  (you can ask for access to enter it yourself) </li>
</ul>

<h4><a name="long-story"></a>Write a longer story</h4>
<ul>
  <li> Story about the work your office does with a staff angle (event, meeting, initiatives, campaign, staff profiles etc.)</li>
  <li> 450 words maximum</li>
  <li>Pre-book a time slot with our team</li>
  <li>Preferably in both English and French</li>
  <li>Allow 24 to 48 hours for publication </li>
  <li>Send up to five pictures (800 pixels minimum and landscape)</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a>  (you can ask for access to enter it yourself) </li>
</ul>

<h4><a name="staff-voices"></a>'Staff Voices' story</h4>
<ul>
  <li>Personal story related to your personal or professional life at the UN</li>
  <li> 450 words maximum</li>
  <li>Preferably in both English and French </li>
  <li>Allow 24 to 48 hours for publication</li>
  <li>Send one to five pictures (800 pixels minimum and landscape)</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a>  (you can ask for access to enter it yourself)</li>
</ul>

<h4><a name="staff-talk"></a>'Staff Talk' story (or podcast)</h4>
<ul>
  <li>Record an interview and share it on iSeek</li>
  <li>150 words maximum for accompanying text (preferably in both English and French )</li>
<li>Pre-book a time slot with our team</li>
  <li>Send an audio file (mp3 format)</li>
  <li>Allow 24 to 48 hours for publication</li>
  <li>Send one to five pictures (800 pixels minimum and landscape)</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a>  (you can ask for access to enter it yourself)</li>
</ul>

<h4><a name="corner-story"></a>'The Corner' Story</h4>
<ul>
  <li>Obituaries about a colleague or a UN mentor who passed away</li>
  <li>450 words maximum</li>
  <li>Pre-book a time slot with our team</li> 
  <li>Preferably in both English and French</li>
  <li>Allow 24 to 48 hours for publication</li>
  <li>Send one to five pictures (800 pixels minimum and landscape)</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a>  (you can ask for access to enter it yourself)</li>
</ul>
<h4><a name="short-news"></a>Short 'news'</h4>
<ul>
  <li>Share a one-line piece of news (launch of a report, new manual available, watch live a meeting or a link to a UN article, etc.)</li>
  <li>20 words maximum</li>
  <li>Preferably in both English and French</li>
  <li>Allow 24 to 48 hours for publication</li>
  <li>Send one to five pictures (800 pixels minimum and landscape)</li>
  <li>Submit the story at <a href="mailto:iseek@un.org">iseek@un.org</a> </li>
</ul>
