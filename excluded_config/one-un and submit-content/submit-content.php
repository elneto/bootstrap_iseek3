<h4>Basic Rules About Content</h4>
  <ul>
      <li>24 to 48 hours before publication</li>
      <li>Submission must be in English and/or French </li>
      <li>All content is subject to revision</li>
      <li>Must follow the applicable requirements (see below)</li>
      <li>Pictures must be 800 pixels minimum and landscape</li>
      <li> Content must adhere to the UN’s core values and <a href="https://undocs.org/ST/SGB/2014/1">Staff Rules and Regulations</a>.</li>
  </ul>
